"""
Generator for notebooks/06_dynamic_factor_model.ipynb  (v2)

Theoretical change from v1:
  - Filter PARAMETERS (loadings + eigenvalues), not factor scores as states.
  - EWMA R_t from FastCovariance as input, not rolling sample cov.
  - Factor alignment (Hungarian + sign + similarity) before every update.
  - Moment-based parameter calibration, no MLE.
  - Contemporaneous factor decomposition (OLS projection onto smoothed loadings).

Run from em_risk_dashboard/ directory.
"""
