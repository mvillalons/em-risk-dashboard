    "def compute_rolling_eigen(",
    "    returns: pd.DataFrame,",
    "    window: int = 252,",
    "    lam: float = 0.94,",
    "    K_min: int = 2,",
    "    K_max: int = 5,",
    "    min_periods: int = 60,",
    ") -> dict:",
    "    \"\"\"",
    "    Compute EWMA R_hat_t at each date, eigendecompose, apply MP threshold.",
    "",
    "    Uses FastCovariance.fit_rolling (single EWMA pass) for R_t.",
    "    D_t (conditional vols) computed in a parallel scalar EWMA pass.",
    "    MP: gamma_t = N / min(t+1, window), sigma2_t = median eigenvalue.",
    "    \"\"\"",
    "    N_assets = returns.shape[1]",
    "    T_full   = len(returns)",
    "    dates_all = returns.index",
    "    X = returns.fillna(0.0).values",
    "",
    "    # Single-pass EWMA for R_t via FastCovariance",
    "    fc = FastCovariance()",
    "    R_series = fc.fit_rolling(returns, lam=lam)  # list of N x N matrices",
    "",
    "    # Parallel scalar EWMA for conditional vols (D_t diagonal)",
    "    burn = min(63, max(T_full // 4, N_assets + 1))",
    "    var_ewma = np.var(X[:burn], axis=0)",
    "    var_ewma = np.maximum(var_ewma, 1e-12)",
    "    d_vecs   = []",
    "    for i in range(T_full):",
    "        var_ewma = lam * var_ewma + (1.0 - lam) * X[i] ** 2",
    "        d_vecs.append(np.sqrt(np.maximum(var_ewma, 1e-12)).copy())",
    "",
    "    V_hat_d      = {}",
    "    lam_hat_d    = {}",
    "    K_t_d        = {}",
    "    lplus_d      = {}",
    "    lnoise_d     = {}",
    "    D_hat_d      = {}",
    "",
    "    for i, dt in enumerate(dates_all):",
    "        if i < min_periods - 1:",
    "            continue",
    "",
    "        R_t   = R_series[i]",
    "        T_eff = min(i + 1, window)",
    "",
    "        # Eigendecompose (eigh returns ascending; sort descending)",
    "        eigvals, eigvecs = np.linalg.eigh(R_t)",
    "        order   = np.argsort(eigvals)[::-1]",
    "        eigvals = eigvals[order]",
    "        eigvecs = eigvecs[:, order]",
    "",
    "        # Marchenko-Pastur threshold",
    "        gamma_t      = N_assets / T_eff",
    "        sigma2_t     = np.median(eigvals)",
    "        lambda_plus  = sigma2_t * (1.0 + np.sqrt(gamma_t)) ** 2",
    "        K_raw = int(np.sum(eigvals > lambda_plus))",
    "        K_t   = max(K_min, min(K_max, K_raw))",
    "",
    "        noise_eigs   = eigvals[K_t:]",
    "        lambda_noise = float(np.mean(noise_eigs)) if len(noise_eigs) > 0 else float(sigma2_t)",
    "",
    "        # Top K_max eigenvectors; sign: largest |element| positive",
    "        V_top = eigvecs[:, :K_max].copy()",
    "        for k in range(K_max):",
    "            if V_top[np.argmax(np.abs(V_top[:, k])), k] < 0:",
    "                V_top[:, k] *= -1",
    "",
    "        V_hat_d[dt]   = V_top",
    "        lam_hat_d[dt] = eigvals[:K_max].copy()",
    "        K_t_d[dt]     = K_t",
    "        lplus_d[dt]   = lambda_plus",
    "        lnoise_d[dt]  = lambda_noise",
    "        D_hat_d[dt]   = d_vecs[i].copy()",
    "",
